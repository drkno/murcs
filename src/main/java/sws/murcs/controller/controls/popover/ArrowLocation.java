package sws.murcs.controller.controls.popover;

/**
 * All possible arrow locations.
 *
 * This control is heavily based and inspired by the one in ControlsFX.
 * Code that has been outright copied is available under the ControlsFX license available here:
 * https://bitbucket.org/controlsfx/controlsfx/src/31432521e87161299b59f9c67ceb5b9ed9ac6c36/license.txt?at=default
 * All remaining code is under the MIT license which is compatible with the above.
 */
public enum ArrowLocation {
    /**
     * Place the arrow in the left top corner.
     */
    LEFT_TOP,

    /**
     * Place the arrow in the left center edge.
     */
    LEFT_CENTER,

    /**
     * Place the arrow in the left bottom corner.
     */
    LEFT_BOTTOM,

    /**
     * Place the arrow in the right top corner.
     */
    RIGHT_TOP,

    /**
     * Place the arrow in the right center edge.
     */
    RIGHT_CENTER,

    /**
     * Place the arrow in the right bottom corner.
     */
    RIGHT_BOTTOM,

    /**
     * Place the arrow in the top left corner.
     */
    TOP_LEFT,

    /**
     * Place the arrow in the top center edge.
     */
    TOP_CENTER,

    /**
     * Place the arrow in the top right corner.
     */
    TOP_RIGHT,

    /**
     * Place the arrow in the bottom left corner.
     */
    BOTTOM_LEFT,

    /**
     * Place the arrow in the bottom center edge.
     */
    BOTTOM_CENTER,

    /**
     * Place the arrow in the bottom right corner.
     */
    BOTTOM_RIGHT;
}
